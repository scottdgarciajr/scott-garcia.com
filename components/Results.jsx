function useCountUp(target, run) {
  const [val, setVal] = React.useState(0);
  React.useEffect(() => {
    if (!run) return;
    const start = performance.now();
    const dur = 1100;
    let raf;
    const tick = (t) => {
      const p = Math.min(1, (t - start) / dur);
      const eased = 1 - Math.pow(1 - p, 3);
      setVal(Math.round(target * eased));
      if (p < 1) raf = requestAnimationFrame(tick);
    };
    raf = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(raf);
  }, [run, target]);
  return val;
}

function Metric({ pre, num, suf, unit, run }) {
  const v = useCountUp(num, run);
  return (
    <div className="metric">
      {pre}{v}{suf}
      {unit && <span className="unit">{unit}</span>}
    </div>
  );
}

function Results() {
  const ref = React.useRef(null);
  const [run, setRun] = React.useState(false);
  React.useEffect(() => {
    if (!ref.current) return;
    const io = new IntersectionObserver(([e]) => { if (e.isIntersecting) setRun(true); }, { threshold: 0.25 });
    io.observe(ref.current);
    return () => io.disconnect();
  }, []);

  return (
    <section className="section wrap" id="results" ref={ref} data-screen-label="03 Results">
      <div className="section-header">
        <div className="eyebrow"><span className="num">§ 03</span> Selected results</div>
        <div>
          <h2 className="h2">Production systems, shipped on a student schedule.</h2>
        </div>
      </div>

      <div className="results-list">
        {RESULTS.map((r) => (
          <div className="result" key={r.n}>
            <div className="num">— {r.n}</div>
            <Metric pre={r.metricPre} num={r.metricNum} suf={r.metricSuf} unit={r.metricUnit} run={run} />
            <div className="desc">{r.desc}</div>
            <div className="source">{r.src}</div>
          </div>
        ))}
      </div>
    </section>
  );
}

window.Results = Results;
